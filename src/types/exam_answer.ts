// Ответы на 116 вопросов зачёта — цитаты из лекций Афонина.
//
// Каждый ответ — массив блоков вида { section, text }, где:
//   - section: пункт лекций (например "п. 1.8", "п. 2.4.1")
//   - text: прямая цитата или очень близкий пересказ структуры
//   - isParaphrase?: true — не дословная цитата, а близкий пересказ
//   - isLectureGap?: true — в лекциях нет прямого ответа, собрано из контекста
//   - gapNote?: string — поясняет где именно лекции молчат и откуда взято
//
// Этот файл читается вместе с questions.json и sourceList:
//   sourceList[n-1].afoninText = тот же вопрос, что и EXAM_ANSWERS[n-1].question
//   EXAM_ANSWERS[n-1].blocks = ответ для раскрывающегося блока под вопросом

export interface AnswerBlock {
  /** Пункт лекций — например "п. 1.8" или "п. 2.4.1, сокращения". */
  section: string;

  /** Текст ответа — цитата или пересказ. Допускает многострочность с \n. */
  text: string;

  /** true — это близкий пересказ, не дословная цитата. Без этого флага — прямая цитата. */
  isParaphrase?: boolean;

  /**
   * true — в лекциях нет прямого ответа на этот вопрос в одном пункте.
   * Ответ собран из контекста, общих процедур или внешних источников (IAMSAR, Регламент МСЭ).
   * Такие блоки стоит показывать пользователю с визуальным маркером.
   */
  isLectureGap?: boolean;

  /** Пояснение в каких пунктах лекций есть и откуда собран ответ (если isLectureGap). */
  gapNote?: string;
}

export interface ExamAnswer {
  /** 1..116 */
  number: number;

  /** Точный текст вопроса из списка Афонина */
  question: string;

  /** Римский номер раздела тренажёра (I..XVII) — совпадает с question bank */
  section: string;

  /** Массив блоков ответа. Обычно 1–3 блока. */
  blocks: AnswerBlock[];
}
